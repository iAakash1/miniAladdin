class SECFilingAnalyzer:
    """Analyzes SEC filings for delisted companies."""
    
    def __init__(self):
        self.base_dir = "sec-edgar-filings"
    
    def analyze_filing(self, ticker: str, form_type: str = "10-K") -> Dict[str, Any]:
        try:
            # Try both ticker and CIK-based paths
            possible_dirs = [
                os.path.join(self.base_dir, ticker.upper(), form_type)
            ]

            # Optional: add CIK fallback if mapping exists
            cik = ticker_to_CIK_mapping.get(ticker)
            if cik:
                possible_dirs.append(os.path.join(self.base_dir, cik.strip(), form_type))

            for filing_dir in possible_dirs:
                if os.path.exists(filing_dir):
                    filings = os.listdir(filing_dir)
                    if filings:
                        latest_filing = sorted(filings)[-1]
                        filing_path = os.path.join(filing_dir, latest_filing, "full-submission.txt")
                        if os.path.exists(filing_path):
                            with open(filing_path, 'r', encoding='utf-8') as f:
                                content = f.read()
                            return {
                                "ticker": ticker,
                                "form_type": form_type,
                                "filing_date": latest_filing,
                                "content_length": len(content),
                                "has_content": bool(content.strip()),
                                "path": filing_path
                            }

            return {"error": f"No {form_type} filings found for {ticker}"}
        
        except Exception as e:
            return {"error": f"Error analyzing {ticker} {form_type}: {str(e)}"}

    
    def analyze_all_filings(self, tickers: List[str], form_type: str = "10-K") -> Dict[str, Any]:
        """Analyze filings for multiple tickers."""
        results = {}
        for ticker in tickers:
            results[ticker] = self.analyze_filing(ticker, form_type)
        return results

