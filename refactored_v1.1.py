import re

def extract_stock_tickers(text: str) -> List[str]:
    """
    Extract stock tickers from AI agent output with improved pattern matching.
    
    Handles formats like:
    - "AAPL: Apple Inc."
    - "TICKER: MSFT (Microsoft Corporation)"
    - Simple ticker lists: "AAPL, MSFT, GOOGL"
    """
    if not text:
        return []

    # 1️⃣ Prefer explicit "Final Selected Tickers:" or similar label lines
    label_match = re.search(r"Final\s+(?:Selected\s+)?[Tt]ickers\s*: *([^\n]+)", text)
    if label_match:
        raw_tokens = [tok.strip() for tok in label_match.group(1).split(',') if tok.strip()]
    else:
        # 2️⃣ Capture bullets / dash-started lines that begin with a ticker symbol
        bullet_pattern = r"(?:^|\n)(?:\d+\.\s*)?[\-\*\•]?\s*([A-Z0-9\.]{1,8})(?=[\s:,.(\-]|$)"
        raw_tokens = re.findall(bullet_pattern, text)

        # 3️⃣ Always attempt comma-separated extraction as well (handles single-line lists)
        for line in text.splitlines():
            if line.count(',') >= 3 and re.fullmatch(r"[A-Z0-9\.\,\s]+", line.strip()):
                comma_tokens = [tok.strip() for tok in line.split(',') if tok.strip()]
                raw_tokens.extend(comma_tokens)
                break

        # If still <3 try numbered list fallback
        if len(raw_tokens) < 3:
            num_pattern = r"(?:^|\n)\d+\.\s*([A-Z0-9\.]{1,8})"
            raw_tokens.extend(re.findall(num_pattern, text))

    exclude_words = {
        'THE', 'AND', 'OR', 'FOR', 'WITH', 'THIS', 'THAT', 'FROM', 'TO', 'OF', 'IN', 'ON', 'AT', 'BY',
        'NOTE', 'KEY', 'CEO', 'CFO', 'CTO', 'BUY', 'SELL', 'HOLD', 'TICKERS', 'TICKER', 'SUMMARY', 'FINAL',
        'NASDAQ', 'NYSE', 'USA'
    }

    seen = set()
    valid_tickers = []
    for tok in raw_tokens:
        tok_up = tok.upper()
        clean = tok_up.replace('.', '')
        if (
            1 <= len(clean) <= 6
            and clean.isalnum()
            and tok_up not in exclude_words
            and tok_up not in seen
        ):
            valid_tickers.append(tok_up)
            seen.add(tok_up)

    return valid_tickers

