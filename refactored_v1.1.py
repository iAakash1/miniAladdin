# Date helper for Finnhub API (last N days)
def _finnhub_date_range(days: int = 30):
    to_dt = datetime.utcnow().date()
    from_dt = to_dt - timedelta(days=days)
    return from_dt.isoformat(), to_dt.isoformat()

def get_finnhub_news(ticker: str = None, category: str = "general", days: int = 30) -> str:
    api_key = os.getenv("FINNHUB_API_KEY")
    if not api_key:
        return "Error: FINNHUB_API_KEY not configured"
    
    def _keyword_filter(headline: str, tick: str) -> bool:
        keywords = [
            "debt", "investigation", "fraud", "delist", "delisting", "bankruptcy", "bankrupt",
            "cash", "production", "earnings", "loss", "sec", "probe", "executive", "leadership",
            "turnover", "lawsuit", "recall", tick.lower() if tick else ""
        ]
        h_lower = headline.lower()
        return any(k in h_lower for k in keywords if k)

    if ticker:
        # Allow caller to specify how far back to look (default 30 days)
        # Signature updated to accept days param via **kwargs but keep backward-compat
        days = locals().get('days', 30)  # fallback if not passed through updated signature
        _from, _to = _finnhub_date_range(days)
        url = f"https://finnhub.io/api/v1/company-news?symbol={ticker}&from={_from}&to={_to}&token={api_key}"
    else:
        url = f"https://finnhub.io/api/v1/news?category={category}&token={api_key}"

    try:
        response = requests.get(url)
        response.raise_for_status()
        articles = response.json()

        # Filter and limit
        if ticker:
            articles = [a for a in articles if _keyword_filter(a.get('headline', ''), ticker)][:15]
        else:
            articles = articles[:10]

        if not articles:
            return f"No relevant news found for {ticker or category}"
        return "\n".join([f"- {a.get('headline', 'No headline')}" for a in articles])
    except Exception as e:
        return f"Error fetching Finnhub news: {str(e)}"


# --- SEC EDGAR Filing Downloader ---
def get_sec_filings_enhanced(ticker: str, form: str = "10-K", limit: int = 3) -> tuple[str, str]:
    """
    Download SEC filings and return both status and directory path.
    Uses CIK mapping when available.
    """
    try:
        email = os.getenv("SEC_EMAIL") or "youremail@example.com"
        company = "Your Company"
        dl = Downloader(company_name=company, email_address=email)
        
        # Try to use CIK if available
        cik = ticker_to_CIK_mapping.get(ticker)
        if isinstance(cik, list):
            cik = cik[0]  # Use first CIK if multiple exist
        
        # Download filings using CIK if available, otherwise use ticker
        if cik:
            dl.get(form, cik.strip(), limit=limit)
            filing_dir = os.path.join("sec-edgar-filings", cik.strip(), form)
        else:
            dl.get(form, ticker, limit=limit)
            filing_dir = os.path.join("sec-edgar-filings", ticker.upper(), form)
        
        status = f"Downloaded {limit} {form} filings for {ticker}"
        return status, filing_dir
        
    except Exception as e:
        error_msg = f"Error downloading {form} for {ticker}: {e}"
        return error_msg, ""

# --- Crew Execution Logic ---
def run_crew_with_handoff(crew: Crew, crew_name: str) -> Dict[str, Any]:
    """
    Run a crew and capture its task outputs using meaningful keys so that
    downstream crews can reliably retrieve them.

    For every task we store the result under TWO keys:
    1. task_<idx>                  → preserves original order if ever needed
    2. <task.name> **or** <agent.role> → human-readable lookup used later
    """
    print(f"Starting {crew_name}…")
    start_time = time.time()

    try:
        result = crew.kickoff()
        duration = time.time() - start_time
        print(f"{crew_name} completed in {duration:.1f} seconds")

        outputs: Dict[str, Any] = {}
        if hasattr(result, 'tasks_output'):
            for i, task_output in enumerate(result.tasks_output):
                # Resolve output value (handle TaskOutput objects from CrewAI 0.1x)
                if hasattr(task_output, 'output'):
                    out_val = task_output.output
                else:
                    out_val = str(task_output)

                # Always keep positional key
                outputs[f'task_{i+1}'] = out_val

                # Human-readable key – prefer explicit task.name, else agent.role
                task_obj = crew.tasks[i]
                human_key = getattr(task_obj, 'name', None) or task_obj.agent.role
                outputs[human_key] = out_val
        else:
            outputs['final_result'] = str(result)

        return outputs

    except Exception as e:
        print(f"Error in {crew_name}: {e}")
        return {"error": str(e)}

